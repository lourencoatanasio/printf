int	ft_declen(int i)
{
	int	n;

	n = 0;
	if (i == 0)
	{
		n++;
	}
	if (i < 0)
	{
		i = -i;
		n++;
	}
	while (i != 0)
	{
		i /= 10;
		n++;
	}
	return (n);
}
